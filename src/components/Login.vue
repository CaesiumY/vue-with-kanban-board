<template>
  <div class="login">
    <h2>Log in to Trello</h2>
    <form @submit.prevent="onSubmit">
      <div>
        <label for="email">Email</label>
        <input
          type="text"
          name="email"
          class="form-control"
          v-model="email"
          autofocus
          placeholder="e.g., test@test.com"
        />
      </div>
      <div>
        <label for="password">Password</label>
        <input
          type="password"
          name="password"
          class="form-control"
          v-model="password"
          placeholder="123123"
        />
      </div>

      <button
        class="btn"
        :class="{ 'btn-success': !invalidForm }"
        :disabled="invalidForm"
      >
        Log In
      </button>
    </form>

    <p class="error" v-if="error">{{ error }}</p>
  </div>
</template>

<script>
export default {
  data() {
    return {
      email: "",
      password: "",
      error: ""
    };
  },

  computed: {
    invalidForm() {
      return !this.email || !this.password;
    }
  },

  methods: {
    onSubmit() {
      console.log(this.email, this.password);
    }
  }
};
</script>

